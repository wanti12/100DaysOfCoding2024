package days04;

public class Days04printdanprintf {
    public static void main(String[] args) {
        String nama="wanti";
        int umur =19;
        System.out.println("contoh penggunaan print:");
        System.out.print("nama: " +nama+" ");
        System.out.print("umur: "+umur);
        
        System.out.println("\n\ncontoh penggunaan printf:");
        System.out.printf("nama saya %s saat ini saya berusia %d tahun",nama,umur);
        System.out.println();
    }
}
